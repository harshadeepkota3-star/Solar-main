// Journey Modal Functionality
(function () {
    'use strict';

    // Wait for DOM to be ready
    document.addEventListener('DOMContentLoaded', function () {
        const modal = document.getElementById('journeyModal');
        const modalBody = modal.querySelector('.journey-modal-body');
        const closeBtn = modal.querySelector('.journey-modal-close');
        const overlay = modal.querySelector('.journey-modal-overlay');
        const readMoreButtons = document.querySelectorAll('.journey-read-more');

        // Open modal function
        function openModal(step) {
            const content = journeyContent[step];
            if (content) {
                modalBody.innerHTML = `
                    <h2>${content.title}</h2>
                    ${content.content}
                `;
                modal.classList.add('active');
                document.body.style.overflow = 'hidden';
            }
        }

        // Close modal function
        function closeModal() {
            modal.classList.remove('active');
            document.body.style.overflow = '';
            setTimeout(() => {
                modalBody.innerHTML = '';
            }, 300);
        }

        // Add click event to all Read More buttons
        readMoreButtons.forEach(button => {
            button.addEventListener('click', function (e) {
                e.preventDefault();
                const step = this.getAttribute('data-step');
                openModal(step);
            });
        });

        // Close modal on close button click
        closeBtn.addEventListener('click', closeModal);

        // Close modal on overlay click
        overlay.addEventListener('click', closeModal);

        // Close modal on Escape key press
        document.addEventListener('keydown', function (e) {
            if (e.key === 'Escape' && modal.classList.contains('active')) {
                closeModal();
            }
        });

        // Prevent modal content click from closing modal
        modal.querySelector('.journey-modal-content').addEventListener('click', function (e) {
            e.stopPropagation();
        });
    });
})();
